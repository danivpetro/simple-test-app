package com.peter.labs.lab2;

public class Task_1 {
    public static void main(String[] args) {
        System.out.println("Цілочисельна арифметика");
        int a = 1 + 1;
        int b = a * 3;
        int c = b / 4;
        int d = c - a;
        int e = -d;
        System.out.println("a=" + a);
        System.out.println("b=" + b);
        System.out.println("c=" + c);
        System.out.println("d=" + d);
        System.out.println("e=" + e);
        System.out.println("Арифметика з використанням  double-типів");
        double double_1 = 1.11 + 1.05;
        double double_2 = double_1 * 3.35;
        double double_3 = double_2 / 2.0;
        double double_4 = double_3 + double_1;
        double double_5 = -double_4;
        System.out.println("double_1= " + double_1);
        System.out.println("double_2= " + double_2);
        System.out.println("double_3= " + double_3);
        System.out.println("double_4= " + double_4);
        System.out.println("double_5= " + double_5);
    }

}
